    Class Post {

		public function categoryList($database){
			
			$array = array();
			$sql = "SELECT * FROM categories";
			$query = mysqli_query($database->con, $sql);
			if (!$query) {
			    header("location: ../cms/index.php?error=true");
			} else {
				while($row = mysqli_fetch_array($query)){
					$array[] = $row;
				}
            }
            
			return $array;
			
		}


